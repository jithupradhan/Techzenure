package com.techzenure.day1;

public class DatatypePractice {

	public static void main(String[] args) {
		int i=50;
		int j=26;
		

		System.out.println(i++);
		System.out.println(i++ + ++i + i-- - --i);
		System.out.println(i);
		
		System.out.println(i+j);
		System.out.println(i-j);
		System.out.println(i/j);
		System.out.println(i%j);
		
		
	}

}

				


